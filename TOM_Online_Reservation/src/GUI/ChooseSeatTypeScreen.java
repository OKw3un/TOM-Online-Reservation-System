    private void confirmAndAddReservation(int seatNumber) {
        int result = JOptionPane.showConfirmDialog(this, "Do you want to reserve seat " + (seatNumber+1) + "?");
        if (result == JOptionPane.YES_OPTION) {
            selectedVoyage.reserveSeat(seatNumber); // Voyage’e koltuğu işaretle
        }
    }

