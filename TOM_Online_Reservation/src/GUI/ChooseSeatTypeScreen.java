    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
            SeatSelectionStrategy strategy = new FirstAvailableSeatStrategy();
            int seatNumber = strategy.selectSeat(selectedVoyage.getSeats());
            confirmAndAddReservation(seatNumber);
            selectedVoyage.reserveSeat(seatNumber);
            currentUser.addReservation(new ReservationData(selectedVoyage, seatNumber+1));
            new HomeScreen(currentUser).setVisible(true);
            this.dispose();

    }//GEN-LAST:event_jButton2ActionPerformed

